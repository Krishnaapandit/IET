//switch case 

#include <stdio.h>
int main()
{
	switch (4 + 4)
	{
	case 7:printf("hii");
	case 8:printf("bye");
	
	case 20:printf("bye-bye");
	break;
	case 22:printf("hellooooo!");

	}
}



